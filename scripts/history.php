<p>
The Amiga section was created near spring of the year 1992 by STC(who was on Nemesis demo-section called Willow).
The idea was to create a cool crew in answer to those sceners who were so serious in the amiga scene.
Sum members from the old french section of Quartz joined and HMD-AMIGA started to make some little productions
and famous packs TCP.
</p>
<br/>
<p>
The crew made some nice prods such as Woualaran which was a technical hard-coding trackmo but HMD's coders
were very very vert lazy ;-). Among the members of this section two have been more successful than others : MADE for its superb
drawings and Gryzor who later coded Pro-Wizard and made the cool "MOds Anthology" CDs!.
</p>
<br/>
<p>
Between 1993-1994, a section PC was created by Black Label, with some
fine production like exemple Lost in Space ( 9th at the Party 3). Marco Polo was  an active member, thanks to
articles on the demo in the ST-MAG and A.C.E BBS. But from 1994 the differents sections stopped one after 
others by lack of time, motivations, etc... the last jump was the demo Geranium on Falcon (atari) in 1995 in
cooperation with EKO (1st at the Gasp party).
</p>
