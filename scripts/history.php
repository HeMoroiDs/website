<p>
&nbsp;"...HMD was founded beetween 1988-1989 by SkyRace and GlUEV ,during a phone discussion they spoke about 
HEMOROIDS as a groupname. For the record, skyRace (14 years old at this time) thought that hemoroids name a star,
and thought it sounded good has his ear, it was far from the true origin of the word ;)). Quickly the group produced lot 
of several small productions. In 1990 HMD made his real first big production participating at the demo-competion
organized by 'GENERATION 4' ,a french game-video magazine. But the production which made HMD known was the Hemosound and later hemosound 2 (An ATARI demo with Amiga mods, those who changed the famous soundchip).
</p>
<br/>
<p>
It was fashion in the early 90 to make alliances with other groups. We were in two alliances:
The Stranglers (HMD+FUZION+IMPACT) and The Guild (HMD+IMPACT). Like all cooperations between groups, we keep very good memories and lots of fun little stories.
</p>
<br/>
<p>
The group also participated in many megademos (demo with several screens of different groups) as exemple
Phaleon Gigademo by NEXT (originally it was a project of The Replicants) or The European demo of THE OVERLANDERS.
Between 1990-1991, new members have arrived, including SINK wich taken over SkyRace (as leader) that they left
the group (for the focus on another computer scene, Cracking and Hacking ).
Gluev (one of the original founder) was kicked 
</p>
<br/>
<br/>
