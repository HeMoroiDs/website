<p>
New era, new members, only SINK was a member at the first generation of the group. At that time in france, there is a
way for people to communicate much like IRC. This was the famous MINITEL, especially RTEL, the server on which the
whole French scene used to discuss and exchange information. This is where SINK met a futur member of the group
STRANGER. Their first meeting took place at a party copy (very frequently at that time). The current went on between
them. The scene at the time was at its highest level.
</p>
<br/>
<p>
Soon, thanks to their many contacts, they have managed to put a group or to join other group in their ranks such as
ST KNIGHTS. The reputation of the group one step further with production increasing qualities as DENTRO, COLORZ
NECROSYS, etc... The group also included the famous magazine AMAZINE (1 single number) or a series of compilations
under the name of SPEEDY PACK.
</p>
<br/>
<p>
The group was present at every party on ST and then a lot happened in France (for ST) of the Transbeauce to
MEGAFUN, you were very lucky to be one of cross members. But after several years of dedication to the demo and ST,
with the arrival of new machinery or the desire to do something else, the group ceased all productions (like all others
scene group). It was a great experience for all and remember full head!
</p>
